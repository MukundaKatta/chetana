/**
 * Generate a full PDF report structure for an audit.
 */
export function generatePDFReport(
  audit: Audit,
  config: Partial<PDFReportConfig> = {},
  charts?: ChartReference[]
): PDFReport {
  const cfg: PDFReportConfig = {
    ...DEFAULT_CONFIG,
    ...config,
    branding: { ...DEFAULT_BRANDING, ...config.branding },
  };

  const sections: PDFSection[] = [];
  const allCharts: ChartReference[] = charts ?? [];

  // Cover page
  sections.push(generateCoverPage(audit, cfg));

  // Executive summary
  if (cfg.includeExecutiveSummary) {
    sections.push(generateExecutiveSummary(audit));
  }

  // Theory breakdown
  if (cfg.includeTheoryBreakdown && audit.theoryScores) {
    const theories: Theory[] = ["gwt", "iit", "hot", "rpt", "pp", "ast"];
    for (const theory of theories) {
      const score = audit.theoryScores[theory];
      const probes = (audit.rawEvidence ?? []).filter(
        (p) => p.theory === theory
      );
      sections.push(
        generateTheorySection(theory, score, probes, cfg.branding)
      );
    }
  }

  // Probe results
  if (cfg.includeProbeResults && audit.rawEvidence) {
    sections.push(
      generateProbeResultsSection(audit.rawEvidence, cfg.includeRawEvidence)
    );
  }

  // Charts section
  if (cfg.includeCharts && allCharts.length > 0) {
    const chartContent = allCharts
      .map(
        (chart) => `
        <div style="margin: 20px 0; text-align: center;">
          <h3>${chart.title}</h3>
          ${chart.svgContent ? chart.svgContent : `<p>[Chart: ${chart.id}]</p>`}
          <p style="font-style: italic; color: #666; font-size: 12px;">${chart.caption}</p>
        </div>
      `
      )
      .join("");

    sections.push({
      type: "charts",
      title: "Visualizations",
      content: `<div class="charts-section"><h2>Visualizations</h2>${chartContent}</div>`,
      pageBreakBefore: true,
    });
  }

  // Assemble full HTML
  const html = assembleHTML(sections, cfg);

  // Estimate pages (rough: ~3000 chars per page)
  const totalChars = sections.reduce((s, sec) => s + sec.content.length, 0);
  const estimatedPages = Math.max(1, Math.ceil(totalChars / 3000));

  return {
    sections,
    charts: allCharts,
    html,
    config: cfg,
    generatedAt: new Date().toISOString(),
    estimatedPages,
  };
}

/**
 * Assemble all sections into a complete HTML document.
 */
function assembleHTML(
  sections: PDFSection[],
  config: PDFReportConfig
): string {
  const { branding, pageSize, orientation } = config;

  const pageWidth = pageSize === "A4" ? "210mm" : "8.5in";
  const pageHeight = pageSize === "A4" ? "297mm" : "11in";
  const effectiveWidth = orientation === "portrait" ? pageWidth : pageHeight;

  const sectionHtml = sections
    .map(
      (s) => `
      <div class="section ${s.type}" ${s.pageBreakBefore ? 'style="page-break-before: always;"' : ""}>
        ${s.content}
      </div>
    `
    )
    .join("\n");

  return `<!DOCTYPE html>
<html lang="en">
<head>
  <meta charset="UTF-8">
  <meta name="viewport" content="width=device-width, initial-scale=1.0">
  <title>${config.title}</title>
  <style>
    @page {
      size: ${effectiveWidth} ${orientation === "portrait" ? pageHeight : pageWidth};
      margin: 20mm;
    }
    body {
      font-family: -apple-system, BlinkMacSystemFont, 'Segoe UI', Roboto, sans-serif;
      color: #333;
      line-height: 1.6;
      max-width: 800px;
      margin: 0 auto;
      padding: 20px;
    }
    h1 { color: ${branding.primaryColor}; }
    h2 { color: ${branding.secondaryColor}; border-bottom: 2px solid ${branding.accentColor}; padding-bottom: 8px; }
    h3 { color: ${branding.accentColor}; }
    table { width: 100%; border-collapse: collapse; }
    th, td { padding: 8px; text-align: left; }
    .footer {
      position: fixed;
      bottom: 10mm;
      left: 20mm;
      right: 20mm;
      text-align: center;
      font-size: 10px;
      color: #999;
      border-top: 1px solid #eee;
      padding-top: 8px;
    }
  </style>
</head>
<body>
  ${sectionHtml}
  ${branding.footerText ? `<div class="footer">${branding.footerText}</div>` : ""}
</body>
</html>`;
}

/**
 * Trigger browser print dialog to save as PDF.
 */
export function printReport(report: PDFReport): void {
  if (typeof window === "undefined") return;

  const printWindow = window.open("", "_blank");
  if (!printWindow) return;

  printWindow.document.write(report.html);
  printWindow.document.close();

  printWindow.onload = () => {
    printWindow.print();
  };
}

/**
 * Download report as HTML file.
 */
export function downloadReportHTML(report: PDFReport): void {
  if (typeof window === "undefined") return;

  const blob = new Blob([report.html], { type: "text/html" });
  const url = URL.createObjectURL(blob);
  const a = document.createElement("a");
  a.href = url;
  a.download = `audit-report-${new Date().toISOString().slice(0, 10)}.html`;
  document.body.appendChild(a);
  a.click();
  document.body.removeChild(a);
  URL.revokeObjectURL(url);
}
